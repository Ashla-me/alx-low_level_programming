#include <stdio.h>

/**
 * main - Prints the alphabet.
 *
 * description:Prints the alphabet
 *
 * Return: Always 0 (Success)
 */

int main()

{
char alphabet[52] = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ";
	int r;

	for (r = 0; r < 52; r++)
	{
		putchar(alphabet[r]);
	}
	while ("r++")
	{
	putchar('\n');
	}
	return (0);
}
